#ifndef __DX_SCENE_H_
#define __DX_SCENE_H_


//****************************************************************************************************
//
//	Scene
//
//****************************************************************************************************
class Scene
{
private:
public:
	//------------------------------------------------------------------------------
	//
	//  @brief		何もしない
	//
	//------------------------------------------------------------------------------
	Scene(){};

	//------------------------------------------------------------------------------
	//
	//  @brief		何もしない
	//
	//------------------------------------------------------------------------------
	virtual ~Scene(){};

	//------------------------------------------------------------------------------
	//
	//  @brief		純粋仮想関数	初期化
	//	@return		true:成功	false:失敗
	//
	//------------------------------------------------------------------------------
	virtual bool Initialize() = 0;
	
	//------------------------------------------------------------------------------
	//
	//  @brief		純粋仮想関数	更新
	//	@return		true:成功	false:失敗
	//
	//------------------------------------------------------------------------------
	virtual bool Update() = 0;
	
	//------------------------------------------------------------------------------
	//
	//  @brief		純粋仮想関数	描画
	//
	//------------------------------------------------------------------------------
	virtual void Render() = 0;

};



//****************************************************************************************************
//
//	NullScene
//
//****************************************************************************************************
class NullScene : public Scene
{
public:
	bool Initialize(){ return true; }
	bool Update(){ return true; }
	void Render(){}


};


#endif // !__DX_SCENE_H_
