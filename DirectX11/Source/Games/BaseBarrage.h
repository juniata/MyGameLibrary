#ifndef __BASE_BARRAGE_H_
#define __BASE_BARRAGE_H_

//****************************************************************************************************
//
//	弾幕クラスの基底クラス
//
//****************************************************************************************************
class BaseBarrage
{
public:
	//------------------------------------------------------------------------------
	//
	//  @brief		デストラクタ
	//
	//------------------------------------------------------------------------------
	virtual ~BaseBarrage();

	//------------------------------------------------------------------------------
	//
	//  @brief		引数付きコンストラクタ
	//  @param[in]	pFilepath	描画する2D画像のパス
	//	@param[in]	instanceNum 描画する2Dオブジェクの数
	//	@param[in]	renderSize	描画サイズ
	//
	//------------------------------------------------------------------------------
	BaseBarrage(const char* pFilepath, const size_t instanceNum, const DirectX::XMFLOAT2& renderSize);

	//------------------------------------------------------------------------------
	//
	//  @brief		更新処理の純粋仮想関数　子クラスで実装してね
	//
	//------------------------------------------------------------------------------
	virtual void DoUpdate() = 0;

	//------------------------------------------------------------------------------
	//
	//  @brief		描画のみを行っている 子クラスで実装する必要があるなら実装してね
	//
	//------------------------------------------------------------------------------
	virtual void DoRender();

	//------------------------------------------------------------------------------
	//
	//  @brief		子クラスで実装された更新関数を実行
	//
	//------------------------------------------------------------------------------
	void Update();

	//------------------------------------------------------------------------------
	//
	//  @brief		子クラスで実装された描画関数を実行
	//
	//------------------------------------------------------------------------------
	void Render();

	//------------------------------------------------------------------------------
	//
	//  @brief		オブジェクトを無効化する
	//
	//------------------------------------------------------------------------------
	void Disable();

	//------------------------------------------------------------------------------
	//
	//  @brief		オブジェクトを有効化する
	//
	//------------------------------------------------------------------------------
	void Enable();

	//------------------------------------------------------------------------------
	//
	//  @brief		オブジェクトが有効かどうか
	//	@return		有効化どうか
	//
	//------------------------------------------------------------------------------
	bool IsEnable() const;

	//------------------------------------------------------------------------------
	//
	//  @brief		オブジェクトが無効かどうか
	//	@return		無効かどうか
	//
	//------------------------------------------------------------------------------
	bool IsDisable() const;
protected:
	//------------------------------------------------------------------------------
	//
	//  @brief		デフォルトコンストラクタ
	//
	//------------------------------------------------------------------------------
	BaseBarrage() {};

	// インスタンス2Dオブジェクト
	DX_Instance2DObject* m_pInstance2D;

	// インスタンス2Dオブジェクトの座標一覧
	DirectX::XMFLOAT3* m_pPotList;

	// インスタンス数
	size_t m_instanecNum;

	// 描画と更新を制御する。デフォルトはfalseになっている
	bool m_bActive;
};

#endif // !__BASE_BARRAGE_H_
