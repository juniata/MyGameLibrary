#ifndef __BULLET_H_
#define __BULLET_H_

//****************************************************************************************************
//
//	Bullet Class
//
//****************************************************************************************************
class Bullet
{
public:
	//------------------------------------------------------------------------------
	//
	//  @brief		初期化
	//
	//------------------------------------------------------------------------------
	Bullet();
	//------------------------------------------------------------------------------
	//
	//  @brief		解放
	//
	//------------------------------------------------------------------------------
	~Bullet();

	//------------------------------------------------------------------------------
	//
	//  @brief		更新
	//	@param[i]	pPos 2Dインスタンス座標
	//	@detail		弾が画面外に行くと、Z値が1以上に設定され描画されないようになる
	//
	//------------------------------------------------------------------------------
	void Update(DirectX::XMFLOAT3* pPos);

	//------------------------------------------------------------------------------
	//
	//  @brief		有効フラグセット
	//  @param[in]  isEnabled セットする有効フラグ(bool)
	//  @detail     引数の値によってUpdateとRenderの動作制御
	//
	//------------------------------------------------------------------------------
	void SetEnabled(bool isEnabled);
	
	//------------------------------------------------------------------------------
	//
	//  @brief		復活させる
	//
	//------------------------------------------------------------------------------
	void Resurrection();

	//------------------------------------------------------------------------------
	//
	//  @brief	    距離加算
	//
	//------------------------------------------------------------------------------
	void SetDistance(float distance);

	//------------------------------------------------------------------------------
	//
	//  @brief	    距離取得
	//
	//------------------------------------------------------------------------------
	float GetDistance();

	//------------------------------------------------------------------------------
	//
	//  @brief		角度加算
	//
	//------------------------------------------------------------------------------
	void SetAngle(float angle);

	//------------------------------------------------------------------------------
	//
	//  @brief		角度取得
	//
	//------------------------------------------------------------------------------
	float GetAngle();
private:
	// 基点
	DirectX::XMFLOAT2 m_basePos;
	// 座標
	DirectX::XMFLOAT2 m_pos;
	// サイズ
	DirectX::XMFLOAT2 m_size;

	int m_interval;
	// 中心からの距離
	float m_distance;
	// 角度
	float m_angle;
	// 有効フラグ
	bool m_isEnabled;
	//------------------------------------------------------------------------------
	//
	//  @brief		移動
	//
	//------------------------------------------------------------------------------
	void Move();

	//------------------------------------------------------------------------------
	//
	//  @brief		リスポーン
	//  @detail     画面外に出るか何かに当たったらリスポーンする
	//
	//------------------------------------------------------------------------------
	void Response();

	//------------------------------------------------------------------------------
	//
	//  @brief	    死亡記録
	//
	//------------------------------------------------------------------------------
	void DeathRecord();
};

#endif