#ifndef __DX_BUTTON_H_
#define __DX_BUTTON_H_


//****************************************************************************************************
//
//	ボタン機能のある2DObject
//
//****************************************************************************************************
class DX_Button : public DX_2DObject
{
public:
	//------------------------------------------------------------------------------
	//
	//  @brief		画像の上でクリックしたかどうか
	//	@return		クリックしたならtrue
	//
	//------------------------------------------------------------------------------
	bool IsClick() const;

	//------------------------------------------------------------------------------
	//
	//  @brief		画像の上でクリックしている最中かどうか
	//	@return		クリックしている最中ならtrue
	//
	//------------------------------------------------------------------------------
	bool IsClickDown() const;

	//------------------------------------------------------------------------------
	//
	//  @brief		画像の上でクリックをしたあとかどうか
	//	@return		クリックしたあとならtrue
	//
	//------------------------------------------------------------------------------
	bool IsClickRelease() const;

private:
	//------------------------------------------------------------------------------
	//
	//  @brief		画像の上にマウスポインタがあるかどうか
	//	@return		あるならtrue
	//
	//------------------------------------------------------------------------------
	bool IsInRect() const;
};
#endif // !__DX_BUTTON_H_
