#pragma once

//	ライティング情報
struct tagLighting {
	DirectX::XMFLOAT4 ambient;
	DirectX::XMFLOAT4 diffuse;
	DirectX::XMFLOAT3 lightPos;
	float padding;	//	16byte alignment
};

//****************************************************************************************************
//
//	Lighting
//
//****************************************************************************************************
class Lighting
{
public:
	//------------------------------------------------------------------------------
	//
	//  @brief		何もしない
	//
	//------------------------------------------------------------------------------
	~Lighting() {}

	//------------------------------------------------------------------------------
	//
	//  @brief		メンバ変数を初期化
	//
	//------------------------------------------------------------------------------
	static void Initialize();

	//------------------------------------------------------------------------------
	//
	//  @brief		環境光を設定
	//	@param[in]	ambient	環境光
	//	@note		rgbの順番 0~1の値
	//
	//------------------------------------------------------------------------------
	static void SetAmbient(const DirectX::XMFLOAT3& ambient);

	//------------------------------------------------------------------------------
	//
	//  @brief		拡散光を設定
	//	@param[in]	diffuse	拡散光
	//	@note		rgbの順番 0~1の値
	//
	//------------------------------------------------------------------------------
	static void SetDiffuse(const DirectX::XMFLOAT3& diffuse);

	//------------------------------------------------------------------------------
	//
	//  @brief		反射光を設定
	//	@param[in]	specular	反射光
	//	@note		rgbの順番 0~1の値
	//
	//------------------------------------------------------------------------------
	static void SetSpecular(const DirectX::XMFLOAT3& specular);

	//------------------------------------------------------------------------------
	//
	//  @brief		ライトの座標を設定
	//	@param[in]	pos	ライト座標
	//
	//------------------------------------------------------------------------------
	static void SetLightPos(const DirectX::XMFLOAT3& pos);

	//------------------------------------------------------------------------------
	//
	//  @brief		頂点シェーダーで行うライト情報を設定
	//
	//------------------------------------------------------------------------------
	static bool SetLightVertexShader();

	//------------------------------------------------------------------------------
	//
	//  @brief		ピクセルシェーダーで行うライト情報を設定
	//
	//------------------------------------------------------------------------------
	static bool SetLightPixelShader();

private:
	static tagLighting m_lightInfo;

	//------------------------------------------------------------------------------
	//
	//  @brief		何もしない
	//
	//------------------------------------------------------------------------------
	Lighting() = delete;
};
