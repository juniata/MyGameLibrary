//****************************************************************************************************
class FrameWork
{
public:
	//------------------------------------------------------------------------------
	//
	//  @brief		メンバー変数の初期化
	//
	//------------------------------------------------------------------------------
	FrameWork();

	//------------------------------------------------------------------------------
	//
	//  @brief		ウィンドウクラスを削除し、メモリを解放
	//
	//------------------------------------------------------------------------------
	~FrameWork();

	//------------------------------------------------------------------------------
	//
	//  @brief		ウィンドウを初期化する
	//
	//------------------------------------------------------------------------------
	bool Initialize();

	//------------------------------------------------------------------------------
	//
	//  @brief		シーンを走らせる(更新＆描画)
	//
	//------------------------------------------------------------------------------
	void Run();
private:
	char*		m_pAppName;
	HINSTANCE	m_hInstance;
	HWND		m_hWnd;

	struct tagFPS {
		int		fps;
		int		count;
		float	deltaTime;
		unsigned long startTime;
	}m_fps;

	//------------------------------------------------------------------------------
	//
	//  @brief		ウィンドウを作成する
	//  @param[in]	width スクリーンの幅	
	//  @param[in]	height スクリーンの高さ
	//	@return		true:成功	false:失敗
	//
	//------------------------------------------------------------------------------
	bool CreateAppWindow(char* pAppName, const unsigned int width, const unsigned int height);

	//------------------------------------------------------------------------------
	//
	//  @brief		FPSを更新する
	//
	//------------------------------------------------------------------------------
	void FPSUpdate();
};