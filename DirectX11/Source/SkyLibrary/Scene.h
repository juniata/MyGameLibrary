#pragma once

//****************************************************************************************************
//
//	Scene
//
//****************************************************************************************************
class Scene
{
public:
	//------------------------------------------------------------------------------
	//
	//  @brief		viewを生成します
	//
	//------------------------------------------------------------------------------
	Scene();

	//------------------------------------------------------------------------------
	//
	//  @brief		viewを解放します
	//
	//------------------------------------------------------------------------------
	virtual ~Scene();

	//------------------------------------------------------------------------------
	//
	//  @brief		初期化
	//	@return		falseの場合エラーが起きたと判断し、ゲームを終了します。
	//
	//------------------------------------------------------------------------------
	virtual bool Initialize() = 0;

	//------------------------------------------------------------------------------
	//
	//  @brief		破棄処理
	//	@return		falseの場合エラーが起きたと判断し、ゲームを終了します。
	//
	//------------------------------------------------------------------------------
	virtual bool Destroy();

	//------------------------------------------------------------------------------
	//
	//  @brief		純粋仮想関数	更新
	//	@return		falseの場合エラーが起きたと判断し、ゲームを終了します。
	//
	//------------------------------------------------------------------------------
	virtual bool Update() = 0;

	//------------------------------------------------------------------------------
	//
	//  @brief		純粋仮想関数	描画
	//	@return		falseの場合エラーが起きたと判断し、ゲームを終了します。
	//
	//------------------------------------------------------------------------------
	virtual bool Render() = 0;
protected:
	View*	m_pView;
};
